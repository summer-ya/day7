package java04_control.loop;

public class For_05 {
	public static void main(String[] args) {

		int sum = 0; //총합 결과
		for(int i = 1; i<=10; i++) { //i, 1~10, x10
		
		sum = sum + i;
		//total += i;
		}
			System.out.println("총합 : " + sum);
			
			
			
			
			//-----------------------------------------------
			
			// 반복 패턴 찾기
			// 반복횟수 : 10번
			// 반복구간 : 1~10
			
			// 초기식 : int i = 1;  (i변수 선언, 반복구간의 시작값으로 대입한다)
			// 조건식 : i <=10; (i변수 판별, 반복횟수보다 작은 조건으로 설정)
			// 증감식 : i++ (1씩 증가)
		}
	}

